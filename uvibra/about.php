                    </li>
                    <li class="post__item">
                        Dois representantes indicados pela <abbr title="Federação das Cooperativas Vinícolas do Rio Grande do Sul">Fecovinho</abbr>
                    </li>
                    <li class="post__item">
                        Um membro, por intermédio da Secretaria de Agricultura, Pecuária e Desenvolvimento Rural, com direito a voz, é facultado ao Estado do RS
                    </li>
