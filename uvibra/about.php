                        Um convidado da <abbr title="Associação Brasileira de Enologia">ABE</abbr>, sem direito a voto
                    </li>
                </ul>
                <p class="post__paragraph">
                    Cabe ao Consevitis deliberar sobre o Plano Anual de Fomento à Vitivinicultura e o orçamento geral de receitas e despesas, observando os objetivos básicos de desenvolvimento harmônico, eficiência e sustentabilidade do segmento vitivinícola do Rio Grande do Sul na busca de resultados devidamente quantificados.
                </p>
                <p class="post__paragraph">
                    Também é incumbência do Conselho providenciar, aprovar e encaminhar à presidência da Uvibra, entidade responsável pela criação do Consevitis, relatório de planejamento e execução de atividades de cada exercício, juntamente com o parecer do Núcleo de Auditoria Estratégica Interna (NEI).
                </p>
                <p class="post__paragraph">
                    Com isso, o foco é contribuir para o desenvolvimento da cadeia produtiva da uva e do vinho e seus derivados, respeitando as especificidades e necessidades, além de ampliar as oportunidades oriundas de possíveis parcerias com o setor público, associações de produtores, entidades técnicas de ensino, pesquisa e extensão. 
                </p>
                <p class="post__paragraph">
                    O Conselho tem trabalhado para uma pauta positiva da vitivinicultura no RS, que contribua para o reconhecimento do setor e seja altamente benéfica para o desenvolvimento do Estado, por meio de projetos que atendam todos os elos
                </p>
                
