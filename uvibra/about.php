                <p class="post__paragraph">
                    O Conselho de Planejamento e Gestão da Aplicação de Recursos Financeiros para Desenvolvimento da Vitivinicultura do Estado do Rio Grande do Sul (Consevitis) foi fundado, em 2019, com o propósito de atuar no desenvolvimento e fortalecimento dos elos de cooperação da cadeia produtiva da uva e do vinho.
                </p>
                <p class="post__paragraph">
                    Desde dezembro de 2020, o Consevitis passou a administrar as ações para aplicação de recursos em três grandes áreas de atuação: a promoção do vinho, o ordenamento setorial e a gestão da cadeia produtiva. Essas ações são executadas a partir de políticas setoriais, aprovadas pelo Fundo de Desenvolvimento da Vitivinicultura do Estado (Fundovitis), criado a partir da Lei 10.989/1997, que dispõe sobre a produção, circulação e comercialização da uva e seus derivados, e têm origem no ICMS pago pelas vinícolas sobre a aquisição das uvas.
                </p>
                <p class="post__paragraph">
                    Entre as atividades estratégicas do Consevitis está o estímulo ao consumo e, consequentemente, a potencialização da venda do vinho nacional, tanto no mercado interno como externo. Dessa forma, a promoção do vinho, especialmente para quem que está aberto a novas experiências, propicia a democratização da bebida de forma equilibrada e saudável, tornando-a cada vez mais popular e presente no dia a dia dos brasileiros.
                </p>
                <p class="post__paragraph">
                    O Conselho conta com participação paritária de produtores de uva, indústria vitivinícola e cooperativas de vinho e é composto por seis membros: 
