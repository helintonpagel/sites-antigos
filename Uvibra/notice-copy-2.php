                    <h3>
                        Dia de Campo: manejo de controle da pérola da terra
                    </h3>
                    <p class="post__paragraph">
                        O primeiro Dia de Campo, de forma presencial, foi realizado no dia 20 de maio e reuniu um grupo de agricultores de Antônio Prado, Ipê, Caxias do Sul e Flores da Cunha, que receberam orientação a respeito do manejo de controle da pérola da terra, inseto nativo da região.
                    </p>
                    <p class="post__paragraph">
                        A propriedade de Valdecir Verdana, em Antônio Prado, foi a área visitada. Amplamente conhecido pelos viticultores, o inseto da pérola da terra se alimenta de alguns açúcares da raiz da videira com maior ênfase na fase de brotação, podendo levar à morte da planta. Agricultores ouviram atentamente as orientações do coordenador técnico da Asstec, Leandro Venturin, que promoveu debate sobre manejos, controles e produção de uva, além de promover uma discussão sobre possibilidades para a redução do custo de produção.Marchioro garantiu compasso na execução do plano previsto, com o apoio dos demais representantes do Conselho, o que tem contribuído para o desenvolvimento da cadeia produtiva da uva e do vinho e seus derivados. “Sempre prezamos pelo respeito as especificidades, necessidades e estágios, além de ampliar as oportunidades oriundas de possíveis parcerias com o setor público, associações de produtores, entidades técnicas de ensino, pesquisa e extensão”. A retomada dos investimentos do Fundovitis vem ao encontro de um momento muito positivo para o setor vitivinícola, visto os bons números registrados em 2020 e os que têm sido apresentados até esse momento de 2021.
                    </p>
