            </h2>
            <p class="brief__paragraph">
                O <b class="brief__stress">Sequence Web Dropshipping</b> é uma ferramenta online que disponibiliza diversas ferramentas para auxiliar na gestão de processos de pequenas e médias empresas. Por se tratar de um sistema WEB você tem acesso de qualquer lugar através de sue navegador, e melhor ainda, não há necessidade de investir em estrutura de tecnologia de informação para utilizá-lo, apenas internet.
            </p>
            <ul class="brief__list">
                <li class="brief__item">
                    Melhora a conexão entre vendedores e distribuidores.
                </li>
                <li class="brief__item">
                    Automatiza a reposição do saldo de produtos pelo fornecedor aos sellers conectados e entre os sellers.
                </li>
                <li class="brief__item">
                    Facilita o desmembramento (<b class="brief__stress">split</b>) de vendas, direcionando cada produto aos deus devidos fornecedores.
                </li>
                <li class="brief__item">
                    Oferece aos sellers a possibilidade de redução do prazo de entrega, visando alavancar a competitividade do e-commerce.
                </li>
                <li class="brief__item">
                    Possibilita ao e-commerce/seller tornar-se um Marketplace.
